<?php

$a = 55.0;
$b = 55;

var_dump($a > $b);

echo "<br>";

var_dump($a < $b);

echo "<br>";

/*
    = Operador de atribuição
    == Operador de comparação de valor
    === Operador de comparação de tipo e valor
*/
var_dump($a === $b);

echo "<br>";

/* 
    != Compara diferença entre o valor das variáveis
    !== Compara a diferença entre o valor e o tipo das variáveis
*/
var_dump($a !== $b);

?>